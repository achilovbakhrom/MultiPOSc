package com.jim.multipos.ui.reports_admin.model;

public class SubReportsCategory {
    String title;
    String description;

    public SubReportsCategory(String title, String description) {
        this.title = title;
        this.description = description;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }
}
